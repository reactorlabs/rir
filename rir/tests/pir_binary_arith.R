generating <- FALSE
if (generating) {
  if (exists("rir.compile"))
    stop("use vanilla GNU R to generate the tests")

  # may depend on options such as printing precision
  ops <- c("+", "-", "*", "/", "%%", "%/%", "^")
  types <- c("lgl", "int", "dbl", "cpl")
  lgls <- c("TRUE", "FALSE", "NA")
  ints <- c("10L", "4L", "1L", "0L", "NA_integer_")
  dbls <- c("10", "4", "1", "0", "NA_real_")
  cpls <- c("3+1i", "1i", "0i", "NA_complex_")

  for (op in ops) {
    for (t1 in types) {
      for (t2 in types) {
        for (lhs in switch(t1,
                           lgl = lgls,
                           int = ints,
                           dbl = dbls,
                           cpl = cpls)) {
          for (rhs in switch(t2,
                             lgl = lgls,
                             int = ints,
                             dbl = dbls,
                             cpl = cpls)) {
            expr <- paste(lhs, op, rhs)
            ok <- TRUE
            res <- tryCatch(eval(str2lang(expr)),
                            error = function(e) ok <<- FALSE)
            if (ok) {
              expected <- paste(res, typeof(res))
              cat("test(function() ", expr, ", \"", expected, "\")\n", sep="")
            }
          }
        }
      }
    }
  }
  q("no")
}
# ================ Test generation ends here ================

testrir <- TRUE

