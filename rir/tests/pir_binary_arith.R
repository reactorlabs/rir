# ================ This generates the tests below ================
if (FALSE) {
  if (exists("rir.compile"))
    stop("use vanilla GNU R to generate the tests")

  ops <- c("+", "-", "*", "/", "%%", "%/%", "^")
  types <- c("lgl", "int", "dbl", "cpl")
  lgls <- c("TRUE", "FALSE", "NA")
  ints <- c("10L", "4L", "0L", "NA_integer_")
  dbls <- c("10", "4", "0", "NA_real_")
  cpls <- c("NA_complex_")

  for (op in ops) {
    for (t1 in types) {
      for (t2 in types) {
        for (lhs in switch(t1,
                           lgl = lgls,
                           int = ints,
                           dbl = dbls,
                           cpl = cpls)) {
          for (rhs in switch(t2,
                             lgl = lgls,
                             int = ints,
                             dbl = dbls,
                             cpl = cpls)) {
            expr <- paste(lhs, op, rhs)
            ok <- TRUE
            res <- tryCatch(eval(str2lang(expr)), error = function(e) ok <<- FALSE)
            if (ok) {
              expected <- paste(res, typeof(res))
              cat("test(function() ", expr, ", \"", expected, "\")\n", sep="")
            }
          }
        }
      }
    }
  }
}
# ================ Test generation ends here ================

if (!exists("rir.compile"))
  rir.compile <- pir.compile <- function(x) x

test <- function(f, expected) {
  f <- rir.compile(f)
  f(); f(); f();
  f <- pir.compile(f)
  res <- f()
  stopifnot(paste(res, typeof(res)) == expected)
}

